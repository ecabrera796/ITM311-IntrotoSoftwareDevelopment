public class EscapeCharacters {
	public static void main(String args[]){
		System.out.println("He said " + "\"" + "Hello" + "\"" + " to me.");
		System.out.println("I said " + "\"" + "Hello" + "\"" + " back." + "\n");
		System.out.print("Hi-Diddle-Dee-Dee. An actor's life for me!" + "\n" +
						 "A high silk hat, and a silver cane." + "\n" + 
						 "A watch of gold, with a diamond chain." + "\n" +
						 "Hi-Diddle-Dee-Day and actor's life is gay!" + "\n" +
						 "It's great to be a celebrity." + "\n" +
						 "An actor's life for me!" + "\n" + "\n" +
						 "Hi-Diddle-Dee-Dum. An actor's life is fun!");
	}
}
